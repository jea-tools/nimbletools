fn draw_scaled_text(
    img: &mut RgbaImage,
    text: &str,
    x: u32,
    y: u32,
    pixel_size: u32,
    color: Rgba<u8>,
) {
    let char_w = 5 * pixel_size + pixel_size; // 5 像素宽 + 1 像素间距
    for (ci, ch) in text.chars().enumerate() {
        if let Some(glyph) = render_char(ch) {
            let base_x = x + ci as u32 * char_w;
            for (row, line) in glyph.iter().enumerate() {
                for (col, &pixel) in line.iter().enumerate() {
                    if pixel == 1 {
                        for dy in 0..pixel_size {
                            for dx in 0..pixel_size {
                                let px = base_x + col as u32 * pixel_size + dx;
                                let py = y + row as u32 * pixel_size + dy;
                                if px < img.width() && py < img.height() {
                                    let existing = img.get_pixel(px, py);
                                    // Alpha 混合
                                    let alpha = color[3] as f32 / 255.0;
                                    let blended = Rgba([
                                        ((color[0] as f32 * alpha
                                            + existing[0] as f32 * (1.0 - alpha))
                                            as u8),
                                        ((color[1] as f32 * alpha
                                            + existing[1] as f32 * (1.0 - alpha))
                                            as u8),
                                        ((color[2] as f32 * alpha
                                            + existing[2] as f32 * (1.0 - alpha))
                                            as u8),
                                        255,
                                    ]);
                                    img.put_pixel(px, py, blended);
                                }
                            }
                        }
                    }
                }
            }
        }
    }
}

fn text_pixel_width(text: &str, pixel_size: u32) -> u32 {
    let char_w = 5 * pixel_size + pixel_size;
    text.len() as u32 * char_w
}

fn text_pixel_height(pixel_size: u32) -> u32 {
    7 * pixel_size
}

#[tauri::command]
pub fn add_watermark(request: WatermarkRequest) -> ProcessResult {
    let mut output_paths = Vec::new();
    let mut errors = Vec::new();

    for input_path in &request.input_paths {
        match image::open(input_path) {
            Ok(img) => {
                let mut rgba_img = img.to_rgba8();
                let (img_w, img_h) = (rgba_img.width(), rgba_img.height());

                // 根据 scale 计算像素大小
                let pixel_size = ((img_w as f32 * request.scale) / 5.0).max(1.0) as u32;
                let tw = text_pixel_width(&request.text, pixel_size);
                let th = text_pixel_height(pixel_size);
                let margin = pixel_size * 3;

                let (x, y) = match request.position.as_str() {
                    "top-left" => (margin, margin),
                    "top-right" => (img_w.saturating_sub(tw + margin), margin),
                    "bottom-left" => (margin, img_h.saturating_sub(th + margin)),
                    "center" => (img_w.saturating_sub(tw) / 2, img_h.saturating_sub(th) / 2),
                    _ => (
                        img_w.saturating_sub(tw + margin),
                        img_h.saturating_sub(th + margin),
                    ),
                };

                let color = Rgba([255, 255, 255, request.opacity]);
                draw_scaled_text(&mut rgba_img, &request.text, x, y, pixel_size, color);

                let stem = Path::new(input_path)
                    .file_stem()
                    .and_then(|s| s.to_str())
                    .unwrap_or("out");
                let ext = Path::new(input_path)
                    .extension()
                    .and_then(|e| e.to_str())
                    .unwrap_or("png");
                let output_path =
                    Path::new(&request.output_dir).join(format!("{}_wm.{}", stem, ext));

                let format = match ext.to_lowercase().as_str() {
                    "jpg" | "jpeg" => ImageFormat::Jpeg,
                    "webp" => ImageFormat::WebP,
                    "bmp" => ImageFormat::Bmp,
                    _ => ImageFormat::Png,
                };

                let dynamic_img = DynamicImage::ImageRgba8(rgba_img);
                match dynamic_img.save_with_format(&output_path, format) {
                    Ok(()) => output_paths.push(output_path.to_string_lossy().to_string()),
                    Err(e) => errors.push(format!("Save error: {}", e)),
                }
            }
            Err(e) => errors.push(format!("Open error: {}", e)),
        }
    }

    ProcessResult {
        success: errors.is_empty(),
        message: if errors.is_empty() {
            format!("Watermarked {} images", output_paths.len())
        } else {
            errors.join("; ")
        },
        output_paths,
    }
}
